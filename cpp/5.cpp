#include <iostream>

// Defining 'std' so it can be used everywhere.
using namespace std;

int main(){
	int x = 10;
	int y = 20;
	cout << "x = " << x << endl; // Print 'x = ' and the value of the var 'x' + terminate the line.
	cout << "y = " << y << endl;
	cout << "\n";

	// Same as previously but formatted more nicely.
	cout << "x = " << x << endl << "y = " << y << endl;
	cout << "\n";

	return 0;
}
